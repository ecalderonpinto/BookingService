package com.reportingtool.controllers.forms;

public class GenerateDMOForm {

	private String period;
	private String amount;
	private String monthLess;

	public String getPeriod() {
		return period;
	}

	public void setPeriod(String period) {
		this.period = period;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getMonthLess() {
		return monthLess;
	}

	public void setMonthLess(String monthLess) {
		this.monthLess = monthLess;
	}
	
	

}
